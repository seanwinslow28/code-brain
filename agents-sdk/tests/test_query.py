def test_run_query_happy_path_with_file_back(tmp_path: Path) -> None:
    vault = _make_vault(tmp_path, _articles_three(), _POPULATED_INDEX)

    def fake_selection(prompt: str, max_tokens: int) -> dict:
        return {
            "selected": [
                {"path": "concepts/error-handling.md", "similarity": 0.83},
                {"path": "connections/error-cache-link.md", "similarity": 0.71},
            ]
        }

    def fake_answer(prompt: str, max_tokens: int) -> str:
        # Validate the answer prompt was built with the article bodies present
        assert "Error Handling" in prompt or "concepts/error-handling.md" in prompt
        return "Yes — see [[concepts/error-handling]]."

    qa_dir = vault / "knowledge" / "qa"
    manifest = qa_dir / ".manifest.json"
    result = run_query(
        question="What's my error handling pattern?",
        vault_root=vault,
        selection_caller=fake_selection,
        answer_caller=fake_answer,
        file_back=True,
        max_articles=5,
        qa_dir=qa_dir,
        manifest_path=manifest,
        db_path=None,
        model_label="qwen3-14b",
        model_route="auto→local",
        now_iso="2026-05-01T14:30:12-04:00",
    )

    assert isinstance(result, QueryResult)
    assert "[[concepts/error-handling]]" in result.answer
    assert len(result.consulted) == 2
    assert result.consulted[0].path == "concepts/error-handling.md"
    assert all(c.chunk_id for c in result.consulted)
    assert result.qa_file is not None
    assert result.qa_file.exists()
    assert result.qa_file.parent == qa_dir
    assert not result.empty_index

    # qa/ frontmatter shape (C.M1)
    qa_text = result.qa_file.read_text(encoding="utf-8")
    assert "model: qwen3-14b" in qa_text
    assert "chunk_id:" in qa_text
    assert "similarity: 0.83" in qa_text
    assert "synth_run: 2026-05-01T14:30:12-04:00" in qa_text

    # Manifest (C.M2): exactly one line, valid JSON, expected keys
    lines = manifest.read_text(encoding="utf-8").splitlines()
    assert len(lines) == 1
    record = json.loads(lines[0])
    assert record["question"] == "What's my error handling pattern?"
    assert record["model"] == "qwen3-14b"
    assert record["model_route"] == "auto→local"
    assert record["qa_file"].endswith(".md")
    assert len(record["consulted"]) == 2
    assert {"path", "chunk_id", "similarity"} <= set(record["consulted"][0])


def test_run_query_empty_index_produces_clean_report_no_qa_file(tmp_path: Path) -> None:
    vault = _make_vault(tmp_path, {}, _EMPTY_INDEX)

    def selection_should_not_run(prompt: str, max_tokens: int) -> dict:
        raise AssertionError("selection caller must not run when index is empty")

