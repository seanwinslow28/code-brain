def test_lookup_chunk_id_for_file(tmp_path: Path) -> None:
    db_path = tmp_path / ".vault-index.db"
    conn = sqlite3.connect(str(db_path))
    conn.execute(
        """CREATE TABLE chunks (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            file_path TEXT NOT NULL,
            chunk_index INTEGER NOT NULL,
            chunk_text TEXT NOT NULL,
            embedding BLOB,
            file_mtime REAL NOT NULL,
            indexed_at TEXT NOT NULL,
            UNIQUE(file_path, chunk_index)
        )"""
    )
    conn.execute(
        "INSERT INTO chunks VALUES (NULL, ?, ?, ?, NULL, 0, '2026-05-01')",
        ("knowledge/concepts/error-handling.md", 0, "first chunk"),
    )
    conn.execute(
        "INSERT INTO chunks VALUES (NULL, ?, ?, ?, NULL, 0, '2026-05-01')",
        ("knowledge/concepts/error-handling.md", 1, "second chunk"),
    )
    conn.commit()
    conn.close()

    cid = lookup_chunk_id_for_file(db_path, "knowledge/concepts/error-handling.md")
    assert cid == compute_chunk_id("knowledge/concepts/error-handling.md", 0)

    # Missing file → None
    assert lookup_chunk_id_for_file(db_path, "knowledge/concepts/missing.md") is None

    # Missing DB → None
    assert lookup_chunk_id_for_file(tmp_path / "nope.db", "anything.md") is None


