# ─── load_articles + chunk_id stamping ────────────────────────────────────


def test_load_articles_stamps_chunk_id_from_db(tmp_path: Path) -> None:
    vault = _make_vault(tmp_path, _articles_three(), _POPULATED_INDEX)
    knowledge = vault / "knowledge"
    db_path = vault / ".vault-index.db"
    conn = sqlite3.connect(str(db_path))
    conn.execute(
        """CREATE TABLE chunks (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            file_path TEXT NOT NULL,
            chunk_index INTEGER NOT NULL,
            chunk_text TEXT NOT NULL,
            embedding BLOB,
            file_mtime REAL NOT NULL,
            indexed_at TEXT NOT NULL,
            UNIQUE(file_path, chunk_index)
        )"""
    )
    conn.execute(
        "INSERT INTO chunks VALUES (NULL, ?, ?, ?, NULL, 0, '2026-05-01')",
        ("knowledge/concepts/error-handling.md", 0, "body"),
    )
    conn.commit()
    conn.close()

    consulted = [
        ConsultedArticle(path="concepts/error-handling.md", chunk_id="", similarity=0.9),
        ConsultedArticle(path="concepts/cache-invalidation.md", chunk_id="", similarity=0.7),
    ]
    pairs = load_articles(knowledge, consulted, db_path)
    assert len(pairs) == 2
    assert all(c.chunk_id for c in consulted)  # both got stamped
    # First one matches the DB-driven id; second one falls back to deterministic
    expected_db = compute_chunk_id("knowledge/concepts/error-handling.md", 0)
    assert consulted[0].chunk_id == expected_db
    expected_fallback = compute_chunk_id("knowledge/concepts/cache-invalidation.md", 0)
    assert consulted[1].chunk_id == expected_fallback


def test_load_articles_skips_missing_files_but_still_stamps_chunk_id(tmp_path: Path) -> None:
    vault = _make_vault(tmp_path, {}, _POPULATED_INDEX)
