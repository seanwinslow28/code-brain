def test_run_selection_pass_filters_invalid_paths_and_clamps_similarity() -> None:
    def fake_llm(prompt: str, max_tokens: int) -> dict:
        return {
            "selected": [
                {"path": "concepts/error-handling.md", "similarity": 0.83},
                {"path": "concepts/error-handling.md", "similarity": 0.5},  # dup, drop
                {"path": "concepts/missing.md", "similarity": 0.99},        # invalid, drop
                {"path": "connections/error-cache-link.md", "similarity": 1.5},  # clamp to 1.0
                {"not_a_dict": True},                                       # malformed
            ]
        }

    consulted, entries = run_selection_pass(
        question="Q?",
        index_text=_POPULATED_INDEX,
        llm_caller=fake_llm,
        max_articles=10,
        selection_max_tokens=500,
    )
    paths = [c.path for c in consulted]
    assert paths == ["concepts/error-handling.md", "connections/error-cache-link.md"]
    assert consulted[0].similarity == 0.83
    assert consulted[1].similarity == 1.0
    assert len(entries) == 3


