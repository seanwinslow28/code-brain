def test_run_query_two_consecutive_file_back_runs_append_two_manifest_lines(tmp_path: Path) -> None:
    vault = _make_vault(tmp_path, _articles_three(), _POPULATED_INDEX)

    def fake_selection(prompt: str, max_tokens: int) -> dict:
        return {"selected": [{"path": "concepts/error-handling.md", "similarity": 0.5}]}

    def fake_answer(prompt: str, max_tokens: int) -> str:
        return "[[concepts/error-handling]]"

    qa_dir = vault / "knowledge" / "qa"
    manifest = qa_dir / ".manifest.json"

    for q, run in [("Question one?", "2026-05-01T14:00:00"),
                   ("Question two?", "2026-05-01T14:15:00")]:
        run_query(
            question=q,
            vault_root=vault,
            selection_caller=fake_selection,
            answer_caller=fake_answer,
            file_back=True,
            qa_dir=qa_dir,
            manifest_path=manifest,
            db_path=None,
            model_label="qwen3-14b",
            model_route="auto→local",
            now_iso=run,
        )

    lines = manifest.read_text(encoding="utf-8").splitlines()
    assert len(lines) == 2
    questions = [json.loads(l)["question"] for l in lines]
    assert questions == ["Question one?", "Question two?"]
    qa_files = sorted(p.name for p in qa_dir.glob("*.md"))
    assert qa_files == ["question-one.md", "question-two.md"]
